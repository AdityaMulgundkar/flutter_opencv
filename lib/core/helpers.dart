/// Helper Size class with width & height doubles
class Size {
  late double x;
  late double y;
  Size(double x, double y) {
    this.x = x;
    this.y = y;
  }
}

/// Helper Point class with x & y axis doubles
class Point {
  late double x;
  late double y;
  Point(double x, double y) {
    this.x = x;
    this.y = y;
  }
}
